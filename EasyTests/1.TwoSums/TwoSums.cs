using System;
using System.Collections.Generic;
using System.Linq;
using System.Threading.Tasks;

namespace 
{
    public class TwoSums
    {
        public int[] TwoSum(int[] nums, int target)
        {
            for(int i = 0; i < nums.lenght; i ++)
            {
                for(int j =0; j < nums.lenght; j++)
                {
                    if(nums[i] + nums[j] = target)
                    {
                        int[] result = nums[i];
                        result = nums[j]
                        return result;
                    }
                }
            }
        }
    }
}